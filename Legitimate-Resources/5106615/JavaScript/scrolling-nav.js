//jQuery to collapse the navbar on scroll
$(window).scroll(function() {
    if ($(".navbar").offset().top > 50) {
        $(".navbar-fixed-top").addClass("top-nav-collapse");
        $(".top-head").addClass("top-head-hidden-sm");
        $(".navbar-brand").addClass("navbar-brand-padding-sm");
        $(".navbar-nav-cust").addClass("push-top");
    } else {
        $(".navbar-fixed-top").removeClass("top-nav-collapse");
        $(".top-head").removeClass("top-head-hidden-sm");
        $(".navbar-brand").removeClass("navbar-brand-padding-sm");
        $(".navbar-nav-cust").removeClass("push-top");
    }
});

//jQuery for page scrolling feature - requires jQuery Easing plugin
$(function() {
    $('a.page-scroll').bind('click', function(event) {
        var $anchor = $(this);
        $('html, body').stop().animate({
            scrollTop: $($anchor.attr('href')).offset().top
        }, 1000, 'easeInOutExpo');
        event.preventDefault();
    });
});

